void UPRDBoolBPLibrary::RandomBoolWithPRD(float Probability, int32 Count, bool& IsSuccess, int32& NewCount)
{
	Probability = round(Probability * 1000.0) / 1000.0;
	Probability = FMath::Clamp(Probability, 0.0f, 1.0f);
	Count++;
	Count = FMath::Max(Count, 1);
	if (FMath::FRand() <= CValues[Probability] * Count) {
		NewCount = 0;
		IsSuccess = true;
		return;
	}
	NewCount = Count;
	IsSuccess = false;
	return;
}
